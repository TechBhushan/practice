package collectiondemo;

import java.util.LinkedHashSet;

public class LinkedHashSetDemo {

	public static void main(String[] args) {
		
		
		LinkedHashSet<Integer> ls=new LinkedHashSet<>();
		ls.add(20);
		ls.add(30);
		ls.add(40);
		ls.add(60);
		ls.add(80);
		ls.add(90);
		
		System.out.println(ls);

	}

}
